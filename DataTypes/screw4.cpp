#include<Stdio.h>
//Data Type Conversion
// One more Example Type Casting
// EXAMPLE 6
//NOTE: INCLUDE THE HEADER FILE stdio.h
int main()
{
    short int sh ;  
    int it ; float ft  = 8.9 ;  

    sh = it = ( ft = ft / 3) * 3 ;

    printf ("short = %d,int = %d ,float = %6.2f \n", sh, it, ft);

    return 0;
}
